public class SuperCalculadora {
    public void adicao(float num1, float num2) {
        System.out.println("\nAdicao de " + num1 + " + " + num2+ " = " + (num1+num2));
    }

    public void subtracao(float num1, float num2) {
        System.out.println("\nSubtracao de " + num1 + " + " + num2+ " = " + (num1-num2));
    }

    public void multiplicacao(float num1, float num2) {
        System.out.println("\nMultiplicacao de " + num1 + " + " + num2+ " = " + (num1*num2));
    }

    public void divisao(float num1, float num2) {
        System.out.println("\nDivisao de " + num1 + " + " + num2+ " = " + (num1/num2));
    }
}
